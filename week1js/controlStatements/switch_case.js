var a =  'a';
switch (a) {
    case "a":
        console.log("A is for apple");
        break;
    case "b":
        console.log("B is for ball");
        break;
    case "c":
        console.log("C is for call");
        break;
    case "d":
        console.log("D is for doll");
        break;
    default:
        console.log("Something went wrong...");   
        break;
}